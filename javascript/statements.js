// If Else
let x = 5;
let y = 5;
if (x >= y) {
  console.log("In If Statement");
} else if (x == y) {
  console.log("In Else if Statement");
} else {
  console.log("In Else Statement");
}

// Switch Case
let marks = 99;

switch (marks) {
  case 98:
    console.log("A");
    break;
  case 99:
    console.log("A+");
    break;
  default:
    console.log("In Default Statement");
}
