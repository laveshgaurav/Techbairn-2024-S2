// for
for (let i = 0; i <= 100; i++) {
  console.log(i);
  if (i == 50) break;
}

let nums = [2, 4, 6, 8, 10];
for (let i = 0; i <= nums.length; i++) {
  console.log(nums[i]);
}

// while
let stoppingVal = 100;
let startingVal = 0;
while (startingVal < stoppingVal) {
  console.log("In While Loop");
  startingVal++;
}
